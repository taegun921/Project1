package AdminLogin;

public class Member {
	private String BobNum;
	private String BobImage;
	private String BobName;
	private String BobPhone;
	private String BobAddress;
	private String BobLocate;
	private String BobMain;
	private String Category;
	private String Date;
	
	public String getBobNum() {
		return BobNum;
	}
	public void setBobNum(String bobNum) {
		BobNum = bobNum;
	}
	public String getBobImage() {
		return BobImage;
	}
	public void setBobImage(String bobImage) {
		BobImage = bobImage;
	}
	public String getBobName() {
		return BobName;
	}
	public void setBobName(String bobName) {
		BobName = bobName;
	}
	public String getBobPhone() {
		return BobPhone;
	}
	public void setBobPhone(String bobPhone) {
		BobPhone = bobPhone;
	}
	public String getBobAddress() {
		return BobAddress;
	}
	public void setBobAddress(String bobAddress) {
		BobAddress = bobAddress;
	}
	public String getBobLocate() {
		return BobLocate;
	}
	public void setBobLocate(String bobLocate) {
		BobLocate = bobLocate;
	}
	public String getBobMain() {
		return BobMain;
	}
	public void setBobMain(String bobMain) {
		BobMain = bobMain;
	}
	public String getCategory() {
		return Category;
	}
	public void setCategory(String category) {
		Category = category;
	}
	public String getDate() {
		return Date;
	}
	public void setDate(String date) {
		Date = date;
	}
}
	